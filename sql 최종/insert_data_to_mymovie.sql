-- M_AvgGrade, U_Time 계산하는 퀴리문 추가하기 --
update MOVIE, (
	select RECORD.M_ID, avg(R_Grade) as avg_g
    from RECORD
    group by M_ID
) as A
set MOVIE.M_AvgGrade = A.avg_g
where MOVIE.M_ID = A.M_ID;

set sql_safe_updates = 0;

update USER, (
	select RECORD.U_ID, sum(M_RunningTime) as sum_r
    from RECORD inner join MOVIE on RECORD.M_ID = MOVIE.M_ID
    group by U_ID
) as B
set USER.U_Time = B.sum_r
where USER.U_ID = B.U_ID;
    
